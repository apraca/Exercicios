package br.com.bootcamp.aula2.TM;

public class Disciplina {
    String nome;
    int carga_hora;

    public Disciplina() {

    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getCarga_hora() {
        return carga_hora;
    }

    public void setCarga_hora(int carga_hora) {
        this.carga_hora = carga_hora;
    }
}
